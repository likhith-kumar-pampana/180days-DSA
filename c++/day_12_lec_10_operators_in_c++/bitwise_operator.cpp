#include<iostream>
using namespace std;

int main()
{

    // & operator

    int ans = 2&3;
    cout<<ans;

    cout<<endl;

    // | operator

    int ans1 = 2|3;
    cout<<ans1 <<endl;

    // ^ xor operator

    int ans2 = 2^3;
    cout<<"my ans is: " << ans2 <<endl;

    // << left shift operator

    int ans3 = 2<<3;
    cout<<"my ans is: " << ans3 <<endl;

    // >> right shift operator

    int ans4 = 18>>3;
    cout<<"my ans is: " << ans4 <<endl;

    // ~ complemet operator

    int ans5 = ~-5;
    cout<<"my ans is: " << ans5 <<endl;


























    cout<<endl;

}